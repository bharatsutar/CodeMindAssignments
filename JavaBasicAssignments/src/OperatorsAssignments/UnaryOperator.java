package OperatorsAssignments;

public class UnaryOperator {
	
	public static void main(String[] args) {
		
		int a = 2;
		
		boolean b = false;
		boolean c = true;
		
		System.out.println(a++); //2 this is individually output

		System.out.println(++a); //3 this is individually output
		
		System.out.println(a--); //2 this is individually output
		
		System.out.println(--a); //1 this is individually output
		
		System.out.println(!b);
		
		System.out.println(!c);
	}

}
